    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // Get the updated data from the text fields
        String updatedFirstName = firstName.getText();
        String updatedLastName = lastName.getText();
        String updatedAge = age.getText();
        String updatedMobile = mobile.getText();
        String updatedNid = nid.getText();
        String updatedDob = dob.getText();
        String updatedAddress = address.getText();

        // Validate non-empty fields
        if (updatedFirstName.isEmpty() || updatedLastName.isEmpty() || updatedAge.isEmpty()
                || updatedMobile.isEmpty() || updatedNid.isEmpty() || updatedDob.isEmpty() || updatedAddress.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all the fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate numeric fields
        try {
            int ageValue = Integer.parseInt(updatedAge);
            int mobileValue = Integer.parseInt(updatedMobile);
            int nidValue = Integer.parseInt(updatedNid);

            // Validate DOB format using regex
            String datePattern = "^(19|20)\\d\\d-(0[1-9]|1[0-2])-(0[1-9]|[12][0-9]|30)$";
            if (!updatedDob.matches(datePattern)) {
                JOptionPane.showMessageDialog(null, "Please enter a valid date of birth in the format yyyy-MM-dd.",
                        "Invalid Input", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Perform the update query in the database
            Connection connection = DatabaseConnection.getConnection();
            PreparedStatement preparedStatement = connection.prepareStatement("UPDATE dbo.Customer "
                    + "SET firstName = ?, lastName = ?, age = ?, Mobile_number = ?, nid = ?, dob = ?, address = ? "
                    + "WHERE customer_id = ?");
            preparedStatement.setString(1, updatedFirstName);
            preparedStatement.setString(2, updatedLastName);
            preparedStatement.setInt(3, ageValue);
            preparedStatement.setInt(4, mobileValue);
            preparedStatement.setInt(5, nidValue);
            preparedStatement.setString(6, updatedDob);
            preparedStatement.setString(7, updatedAddress);
            preparedStatement.setInt(8, customerId);

            int rowsAffected = preparedStatement.executeUpdate();

            // Check if the update was successful
            if (rowsAffected > 0) {
                // Refresh the table after successful update
                fetchAndPopulateData(customerId);
                JOptionPane.showMessageDialog(null, "Data updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

                setVisible(false);
                CustomersJFrame frame = new CustomersJFrame();
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to update data.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            preparedStatement.close();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please enter valid numeric values for Age, Mobile Number, and NID.",
                    "Invalid Input", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_updateActionPerformed

