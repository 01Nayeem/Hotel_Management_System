    private void fetchAndPopulateData(int customerId) {
        try {
            Connection connection = DatabaseConnection.getConnection();
            PreparedStatement preparedStatement = connection.prepareStatement("SELECT * FROM dbo.Customer WHERE customer_id = ?");
            preparedStatement.setInt(1, customerId);
            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                String firstNameText = resultSet.getString("firstName");
                String lastNameText = resultSet.getString("lastName");
                String ageText = resultSet.getString("age");
                int mobileNumber = resultSet.getInt("Mobile_number");
                int nidNumber = resultSet.getInt("nid");
                String dobText = resultSet.getString("dob");
                String addressText = resultSet.getString("address");

                // Update the text fields with the fetched data
                firstName.setText(firstNameText);
                lastName.setText(lastNameText);
                age.setText(ageText);
                mobile.setText(String.valueOf(mobileNumber));
                nid.setText(String.valueOf(nidNumber));
                dob.setText(dobText);
                address.setText(addressText);
            } else {
                // Handle the case when no matching user is found
                // Clear the text fields or show an appropriate message
                firstName.setText("");
                lastName.setText("");
                age.setText("");
                mobile.setText("");
                nid.setText("");
                dob.setText("");
                address.setText("");
            }

            resultSet.close();
            preparedStatement.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

